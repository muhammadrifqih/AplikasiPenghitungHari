    private void updateCalendarDate() {
        int bulan = jComboBox1.getSelectedIndex() + 1; // Indeks Januari dimulai dari 1
        int tahun = (int) jSpinner1.getValue();

        // Atur tanggal ke hari pertama dari bulan dan tahun yang dipilih
        LocalDate selectedDate = LocalDate.of(tahun, bulan, 1);
        jCalendar2.setDate(java.util.Date.from(selectedDate.atStartOfDay(ZoneId.systemDefault()).toInstant()));
    }

        private void updateSpinnerAndComboBoxFromCalendar() {
        LocalDate selectedDate = jCalendar1.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        
        // Update nilai combo box bulan dan spinner tahun sesuai dengan tanggal di kalender
        jComboBox1.setSelectedIndex(selectedDate.getMonthValue() - 1); // Januari = 0
        jSpinner1.setValue(selectedDate.getYear());
    }
    
