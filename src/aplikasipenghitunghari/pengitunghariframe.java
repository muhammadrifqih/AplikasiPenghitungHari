    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int bulan = jComboBox1.getSelectedIndex() + 1; // Indeks Januari dimulai dari 1
        int tahun = (int) jSpinner1.getValue();
        LocalDate date = LocalDate.of(tahun, bulan, 1);

        // Hitung jumlah hari dalam bulan
        int jumlahHari = date.lengthOfMonth();
        jLabel3.setText("Jumlah Hari: " + jumlahHari);

        // Hari pertama dan terakhir
        LocalDate hariPertama = LocalDate.of(tahun, bulan, 1);
        LocalDate hariTerakhir = LocalDate.of(tahun, bulan, jumlahHari);
        jLabel4.setText("Hari pertama: " + hariPertama.getDayOfWeek().getDisplayName(TextStyle.FULL, Locale.getDefault()) +
            ", Hari terakhir: " + hariTerakhir.getDayOfWeek().getDisplayName(TextStyle.FULL, Locale.getDefault()));

        boolean isLeapYear = date.isLeapYear();
        jLabel5.setText (isLeapYear ? "Tahun " + tahun + " adalah tahun kabisat." : "Tahun " + tahun + " bukan tahun kabisat.");

        // Menghitung selisih hari antara tanggal awal dan tanggal akhir
        LocalDate tanggalAwal = LocalDate.ofInstant(jCalendar1.getDate().toInstant(), ZoneId.systemDefault());
        LocalDate tanggalAkhir = LocalDate.ofInstant(jCalendar2.getDate().toInstant(), ZoneId.systemDefault());
        long selisihHari = ChronoUnit.DAYS.between(tanggalAwal, tanggalAkhir);
        jLabel7.setText("Selisih hari antara tanggal awal dan akhir: " + selisihHari + " hari.");
    }//GEN-LAST:event_jButton1ActionPerformed

